export function ackMockAlert(alert_id, user) {
  MOCK_ALERTS = MOCK_ALERTS.map((a) =>
    a.alert_id === alert_id
      ? { ...a, status: "Acknowledged", ack_time: new Date().toISOString(), ack_user: user, ack_source: "Dashboard" }
      : a
  );
}

